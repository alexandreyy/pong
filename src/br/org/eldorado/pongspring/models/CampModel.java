package br.org.eldorado.pongspring.models;

public class CampModel {
	private int width;
	private int height;

	public CampModel(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
